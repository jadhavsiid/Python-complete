
# ^ String Slicing
course = "Python Introduction"
# ! slice- "Intro" using start and end index
print(course[7:12])
# ! slice- "Python" using slice from the start
print(course[:6])
# ! slice- "Introduction" using slice to the end
print(course[7:])
# ! slice- "duct" using -ve indexing
print(course[-7:-3])